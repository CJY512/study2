    @PersistenceUnit
    EntityManagerFactory emf;

    @DisplayName("페치 조인 (미적용)")
    @Test
    void fetchJoinNo() throws Exception {
        em.flush();
        em.clear();

        Member member1 = queryFactory
                .selectFrom(member)
                .where(member.username.eq("member1"))
                .fetchOne();
        boolean isLoaded = emf.getPersistenceUnitUtil().isLoaded(member1.getTeam());

        assertThat(isLoaded).as("페치 조인 미적용").isFalse();

    }

    @DisplayName("페치 조인 적용")
    @Test
    void fetchJoinUse() throws Exception {
        em.flush();
        em.clear();

        Member member1 = queryFactory
                .selectFrom(member)
                .join(member.team, team).fetchJoin()
                .where(member.username.eq("member1"))
                .fetchOne();

        boolean loaded = emf.getPersistenceUnitUtil().isLoaded(member1.getTeam());
        assertThat(loaded).as("페치 조인 적용").isTrue();
    }

    /**
     * 나이가 가장 많은 회원 조회
     */
    @DisplayName("서브쿼리")
    @Test
    void subquery() throws Exception {
        QMember memberSub = new QMember("memberSub");

        List<Member> result = queryFactory
                .selectFrom(member)
                .where(member.age.eq(
                        select(memberSub.age.max())
                                .from(memberSub)
                ))
                .fetch();

        assertThat(result).extracting("age")
                .containsExactly(40);

    }

    /**
     * 나이가 평균 나이 이상인 회원
     */
    @DisplayName("서브쿼리2")
    @Test
    void subQueryGoe() throws Exception {
        QMember memberSub = new QMember("memberSub");

        List<Member> result = queryFactory
                .selectFrom(member)
                .where(member.age.goe(
                        select(memberSub.age.avg())
                                .from(memberSub)
                ))
                .fetch();

        assertThat(result)
                .extracting("age")
                .containsExactlyInAnyOrder(30, 40);
    }

